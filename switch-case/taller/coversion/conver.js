let num = parseFloat(prompt("Ingrese el numero que quiera convertir"));

switch (num) {
  case 1:
    console.log("tienes un gato");
    break;
  case 2:
    console.log("Tiene un perro");
    break;
  case 3:
    console.log("Tiene un pajaro");
    break;
  default:
    console.log("No dijo un animal del menu");
    break;
}


